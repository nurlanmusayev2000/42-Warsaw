int	ft_sqrt(int nb)
{
	int	x;

	x = 1;
	while (x <= nb)
	{
		if (x * x == nb)
		{
			return (x);
		}
		else if (x == nb)
		{
			return (0);
		}
		x++;
	}
	return (0);
}
